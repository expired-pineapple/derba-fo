<script lang="ts" setup>
import { ref, onBeforeMount } from "vue"
import { useStore } from 'vuex'
import { useRoute } from "vue-router"
import router from "@/router"

const store = useStore()
const route = useRoute()
const searchValue = ref('')
const id = route.params.id

const loading = ref(true)
const expanded = ref(false)

const successAlert = ref(false)
const errorAlert = ref(false)

const data = ref({})
const bolo = ref([])
const trucks = ref([])
const fleets =ref([])

const dispatch = async () => {
  try {
    await store.dispatch("fetchFleetBoloes")
    await store.dispatch("fetchFleetBolo", id)
    await store.dispatch("fetchTrucks")
    await store.dispatch("fetchFleets")
    trucks.value = store.getters.trucks
    fleets.value = store.getters.fleets
    
    data.value = store.getters.fleetBoloes
    bolo.value = store.getters.fleetBolo
  } catch (err) {
    console.error('Error dispatching in truck form:', err)
  } finally {
    loading.value = false
  }
}

onBeforeMount(async () => {
  await dispatch()
})

const searchFleet = () => {
  const search = searchValue.value

  const filteredBoloes = data.value.filter(item => {
    return item.FltBolo_no.toLowerCase().includes(search.toLowerCase())
  })

  if(!search){
    return data.value = store.getters.fleetBoloes
  }
  data.value = filteredBoloes
  
  
}

const edit = async item => {
  router.push({ name: 'edit-fleet-bolo', params: { id: item.id } })
  await store.dispatch('fetchFleetBolo', item.id)
  bolo.value = store.getters.fleetBolo
}

const editSelected = item => {
  edit(item)
}

const submitForm = async () => {
  try {
    await store.dispatch('updateFleetBolo', bolo)
    successAlert.value = true
    setTimeout(() => {
      successAlert.value = false
    }, 3000)
    await dispatch()
  } catch (err) {
    console.error('Error submitting form:', err)
    errorAlert.value = true
    setTimeout(() => {
      errorAlert.value = false
    }, 3000)
  }
  await dispatch()
}

const deleteItem = async item => {
  await store.dispatch('deleteFleetBolo', item.id)
  await dispatch()
  router.push('/fleet-bolo')
}

const disabled = ref(true)
const dialog = ref(false)

const editEnabled = () => {
  disabled.value = false
}

const isEmptyValidator = value => {
  if (!value) {
    return "This field is required."
  }
  
  return true
}

const hasExpired = value =>{
  const date = new Date(value)
  if(date < new Date()){
    return "Document has expired"
  }
}
</script>

<template>
  <div class="d-flex">
    <VCard
      v-if="expanded"
      class="mr-4"
      width="35%"
    >
      <div class="searchable">
        <VCardText> 
          <div class="d-flex">
            <VTextField
              v-model="searchValue"
              prepend-inner-icon="mdi-magnify"
              label="Search"
              variant="underlined"
              class="mb-4"
              @input="searchFleet"
            />
          </div>
          <div
            v-if="loading"
            class="preloader"
          >
            <div
              v-for="(item, index) in 6"
              :key="index"
            >
              <div class="header" />
              <div class="sub" />
            </div>
          </div>
          <div
            v-if="data.length > 0"
            class="wrapper scrollable"
          >
            <div
              v-for="(item, index) in data"
              :key="index"
            >
              <div class="d-flex mt-4">
                <VIcon
                  icon="mdi-truck-outline" 
                  size="30"
                  class="mr-2"
                  color="primary"
                />  
                <div>
                  <div class="d-flex">
                    <div class="info">
                      <span class="font-weight-semibold me-2 mb-4">Fleet: {{ item.FltBolo_no }}</span>
                    </div>
                    <div class="icon">
                      <VBtn
                        icon="mdi-pencil"
                        size="x-small"
                        variant="tonal"
                        @click="editSelected(item)"
                      />
                    </div>
                  </div>
                </div>
              </div>
              <VDivider />
            </div>
          </div>
          <div
            v-else
            class="else"
          >
            <p>No Fleet Found</p>
          </div>
        </VCardText>
      </div>
    </VCard>

    <VCard
      class="d-flex gap-6"
      width="100%"
    >
      <div class="">
        <VCardText>
          <VRow width="20%">
            <VCol cols="12">
              <VAlert
                v-model="successAlert"
                border="start"
                variant="tonal"
                closable
                close-label="Close Alert"
                type="success"
                title="Success!"
                text="Fleet details saved successfully"
              />
              <VAlert
                v-model="errorAlert"
                border="start"
                variant="tonal"
                closable
                close-label="Close Alert"
                type="error"
                title="Error!"
                text="Fleet details not saved successfully"
              />
              <!-- ðŸ‘‰ Form -->
              <VForm
                class="mt-10"
                :disabled="disabled"
                :loading="loading"
                @submit.prevent="submitForm"
              >
                <VCardText>
                  <div class="icons d-flex justify-end">
                    <VIcon
                      size="24"
                      icon="mdi-pencil-outline"
                      class="me-6"
                      @click="editEnabled"
                    />
                    <VDialog
                      v-model="dialog"
                      persistent
                      width="auto"
                    >
                      <template #activator="{ props }">
                        <VIcon
                          size="24"
                          icon="mdi-delete-outline"
                          class="me-6"
                          v-bind="props"
                          color="error"
                        />
                      </template>
                      <VCard>
                        <VCardTitle class="headline">
                          Delete Bolo
                        </VCardTitle>
                        <VCardText>
                          Are you sure you want to delete this bolo?
                        </VCardText>
                        <VCardActions>
                          <VSpacer />
                          <VBtn
                            color="green-darken-1"
                            variant="text"
                            @click="dialog = false"
                          >
                            No
                          </VBtn>
                          <VBtn
                            color="error"
                            variant="text"
                            @click="deleteItem(bolo)"
                          >
                            Yes
                          </VBtn>
                        </VCardActions>
                      </VCard>
                    </VDialog>
                  </div>
                  <div class="d-flex">
                    <VIcon
                      size="70"
                      icon="mdi-truck-outline"
                      class="me-6"  
                    />
                    <div>
                      <h3 class="font-weight-semibold mb-2">
                        Fleet Bolo Details
                      </h3>
                      <p class="mb-2">
                        Please fill in the form below to edit selected fleet bolo
                      </p>
                    </div>
                  </div>
                </VCardText>
                <VCardText>
                  <VRow>
                    <VCol
                      md="6"
                      cols="12"
                    >
                      <VSelect
                        v-model="bolo.TrkId"
                        :items="trucks"
                        item-value="id"
                        item-title="FltId.fltFleetNo"
                        :loading="loading"
                        label="Truck"
                        required
                        :rules="[isEmptyValidator]"
                      />
                    </VCol>
                    <VCol
                      md="6"
                      cols="12"
                    >
                      <VSelect
                        v-model="bolo.FltId"
                        :items="fleets"
                        item-value="id"
                        item-title="fltPlateNo"
                        label="Fleet"
                        required
                        persistent-hint="Fleet plate number"
                        :loading="loading"
                        :rules="[isEmptyValidator]"
                      />
                    </VCol>
                    <VCol
                      md="6"
                      cols="12"
                    >
                      <VTextField
                        v-model="bolo.FltBolo_no"
                        label="Bolo Number"
                        required
                        :loading="loading"
                        :rules="[isEmptyValidator]"
                      />
                    </VCol>
                    <VCol
                      md="6"
                      cols="12"
                    >
                      <VTextField
                        v-model="bolo.FltBoloissuedate"
                        label="Bolo Issued Date"
                        required
                        type="date"
                        :loading="loading"
                        :rules="[isEmptyValidator]"
                      />
                    </VCol>
                    <VCol
                      md="6"
                      cols="12"
                    >
                      <VTextField
                        v-model="bolo.FltBoloExpireDate"
                        label="Bolo Expiry Date"
                        required
                        type="date"
                        :loading="loading"
                        :rules="[isEmptyValidator, hasExpired]"
                      />
                    </VCol>
                    <VCol
                      md="6"
                      cols="12"
                    >
                      <VSelect
                        v-model="bolo.FltBoloActive"
                        :items="[{text: 'Active', value: true}, {text: 'Inactive', value: false}]"
                        item-value="value"
                        item-title="text"
                        label="Bolo Status"
                        required
                        :rules="[isEmptyValidator]"
                      />
                    </VCol>
  
                    <VCol
                      cols="12"
                      class="d-flex flex-wrap gap-4"
                    >
                      <VBtn @click.prevent="submitForm">
                        Save
                      </VBtn>
                    </VCol>
                  </VRow>
                </VCardText>
              </VForm>
            </VCol>
          </VRow>
        </VCardText> 
      </div> 
    
      <div class="justify-end mt-4">
        <VIcon 
          color="primary"
          :icon="expanded ? 'mdi-arrow-expand' :'mdi-arrow-collapse'"
          @click="expanded=!expanded"
        />
      </div>     
    </VCard>
  </div>
</template>

